	SUBROUTINE IONDIST (TDZ,XNDZ,XNOZ,SATZ)
	INCLUDE 'SOLDIV.FI'
		RATNENI2D=(1.+ IZINJECT*FZINJECT+4.*FBE+2.*FHE+
     2			IZINTRIN*FZINTRIN)**2
     	CNEUT = 1.
	IF(IOPTELN.EQ.0) CNEUT = 0.
C		HIGH RECYCLING REGION IN FRONT OF DIVERTOR PLATE
	TDD = TDZ		
	IF(TDD.LT.1.E-1) TDD = 1.05E-1 
	IF(TDD.GT.1E3) TDD = .95E3
     	XNDD = XNDZ
 	IF(XNDZ.GT.1E22) XNDD = 0.95E22
	IF(XNDZ.LT.1E16) XNDD = 1.1E16
	TND = TDD
	IF(TND.GE.1000) TND = 995.
	CALL INTERP(TDD,TDD,TND,XNDD)
	SVELZ = SEL(1)
	SVELZN= SELN(1)
	SVCXZ = SCX(1)
	XNODZ = XNOZ
	SVATZ = SEL(1) + CNEUT*SELN(1)*XNODZ/XNDD + SCX(1)
	SVIONZ = SION(1) 
	SVRECZ = RECOM(1)
	IF(XNDD.LE.1.E21)
     2	EIONZ = 17.5 + (5.+37.5/TDD)*LOG10(1.E21/(XNDD))
	iF(XNDD.GT.1.E21)
     2    EIONZD = (30.6 - 16.4*EXP(-5.E19/XNDD))*
     3              EXP(5.45/(TDD*EXP((XNDD/1.37E20)**0.26)))
	SATZ = XK*EIONZ*XNDD*XNODZ*SVIONZ -XK*FREC*PION*SVRECZ +
	1		 1.5*XNDD*XNODZ*XK*(TDD)

	RETURN
	END