	SUBROUTINE NEUTLSND
	INCLUDE 'SOLDIV.FI'
	DIMENSION AMAT(100,100),SOURCE (100), THETA(15)
C		TEP EXTENDED NEUTRAL MODEL 6/09	FOR LSND & 1D SOL-DIV MODEL
C	GEOMETRY
	R0 = RMAJOR
	Z0 = ZCENTER
	AM = AMINOR
	RW(11) = RX
	ZW(11) = ZX
C	POLOIDAL ANGLE
	DO 100 N = 1,15
	THETA(N) = ATAN((ZW(N)-Z0)/(ABS(RW(N)-R0)))
100	CONTINUE
C	(R,Z) COORDINATES OF CORNERS OF PED & SOL REGIONS
C		SHAFRANOV SHIFT PARAMETER
	BETAP = 2.6E-6*XNTB*XK*XTTB/((BFIELD*BETAG)**2)
	XLIND = LOG(1.65 +0.89*(Q95-1.0))
	DELP = -(AM/R0)*(BETAP +XLIND/2.)
	DO 150 N = 1,15

	X =
	RP(1,N) = (R0-3.*DELP*DR) + (AM-3.*DR)*COS(THETA(N)+X*SIN(THETA(N)))
	RP(2,N) = (R0-2.*DELP*DR) + (AM-2.*DR)*COS(THETA(N)+X*SIN(THETA(N)))
	RP(3,N) = (R0-1.*DELP*DR) + (AM-1.*DR)*COS(THETA(N)+X*SIN(THETA(N)))
	RP(4,N) = (R0-0.*DELP*DR) + (AM-0.*DR)*COS(THETA(N)+X*SIN(THETA(N)))
	RP(5,N) = (R0+1.*DELP*DS) + (AM+1.*DS)*COS(THETA(N)+X*SIN(THETA(N)))
	RP(6,N) = (R0+2.*DELP*DS) + (AM+2.*DR)*COS(THETA(N)+X*SIN(THETA(N)))
	RETURN
	END